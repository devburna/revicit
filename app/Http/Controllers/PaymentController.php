    {
        // get company wallet
        $profile = Company::where('company_id', $request->company_id)->firstOrfail();

        // check policy
        if (!$request->user()->is($profile->company->user)) {
            abort(403, 'This action is unauthorized.');
        }

        return $this->show($profile);
    }

